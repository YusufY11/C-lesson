#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{
    /*
    ceil floor sqrt pow abs
    */

    printf("%f\n",ceil(7.05));  /*Yukarýya dogru yuvarlama*/

    printf("%f\n",floor(11.8));  /*Taban a dogru yani assagiya yuvarlama*/

    printf("%f\n",sqrt(75));  /*Bir sayinin kare kokunu hesaplama*/

    printf("%f\n",pow(2,4));  /*Usslu sayýlar once taban sayýsý sonra ussu sayýsý yazýlýr*/

    printf("%d\n",abs(5)); /* Mutlak deðer*/
    return 0;
}
